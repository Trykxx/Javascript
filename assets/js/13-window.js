/* ------------------------------ OBJETS ------------------------------------- */

/*
    L'objet window en Javascript est un objet global qui represente la fenetre du navigateur
    Cet objet est crée automatiquement lorsque la page web est chargée
*/

/*
    L'objet window en Javascript est un objet important car il fournit une interface entre le code Javascript et la fenetre du navigateur
*/
console.log(window);

// La propriété 'window.console' permet d'accéder a l'objet console
console.log(window.console);

// La propriété 'window.alert()' permet d'afficher une boite de dialogue
window.alert('Hello, world!');

// La propriété 'window.document' permet d'accéder a l'onglet document
console.log(window.document);